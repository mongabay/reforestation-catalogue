export interface LoadingSpinnerProps {
  /** Set the loading background transparent. Default to `false`. */
  transparent?: boolean;
  /** Set the loading spinner inside a box. Default to `false`. */
  inner?: boolean;
  /** Make the loading spinner behave like an inline-block. Default to `false`. */
  inline?: boolean;
  //**  Reduced size of the loading spinner. Default to `false`. */
  mini?: boolean;
}
